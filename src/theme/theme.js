export const getStatusColor = (status) => {
  const colors = {
    pending: theme.colors.warning[500],
    assigned: theme.colors.primary[500],
    in_progress: theme.colors.secondary[500],
    resolved: theme.colors.success[500],
    rejected: theme.colors.error[500],
    quoted: theme.colors.neutral[500],
    selected: theme.colors.success[500],
  };
  return colors[status] || theme.colors.neutral[500];
};

export const getPriorityColor = (priority) => {
  const colors = {
    low: theme.colors.success[500],
    medium: theme.colors.warning[500],
    high: theme.colors.error[500],
  };
  return colors[priority] || theme.colors.neutral[500];
};
